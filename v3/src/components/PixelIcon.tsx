export type PixelIconName = keyof typeof PIXEL_ICONS;

// ---------------------------------------------------------------------------
// Component
// ---------------------------------------------------------------------------

interface PixelIconProps {
  name: PixelIconName;
  size?: number;
  color?: string;
  /** Additional CSS class applied to the SVG element. */
  class?: string;
  style?: JSX.CSSProperties;
}

/**
 * Renders a crisp 16×16 pixel-art icon as an SVG.
 * Set `size` to scale (default 16). Color inherits from `currentColor`.
 */
export function PixelIcon({
  name,
  size = 16,
  color = 'currentColor',
  class: className,
  style,
}: PixelIconProps): JSX.Element {
  const cells = PIXEL_ICONS[name] as readonly string[];
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      role="img"
      aria-label={name}
      class={className}
      style={{ display: 'block', ...style }}
    >
      {cells.map((c, i) => {
        const spaceIdx = c.indexOf(' ');
        const x = parseInt(c.slice(0, spaceIdx), 10);
        const y = parseInt(c.slice(spaceIdx + 1), 10);
        return (
          <rect
            key={i}
            x={x}
            y={y}
            width={1}
            height={1}
            fill={color}
            shape-rendering="crispEdges"
          />
        );
      })}
    </svg>
  );
}
